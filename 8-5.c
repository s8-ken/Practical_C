/********************************************
練習問題8-5
アルファベットを一文字読み、
それが母音か子音か判定するプログラム。
小文字のみに対応
********************************************/


#include <stdio.h>

int main(){
  char line[128];

  printf("小文字アルファベットを一文字入力してください : ");
  fgets(line, sizeof(line), stdin);

  if(line[0] == 'a'||
     line[0] == 'e'||
     line[0] == 'i'||
     line[0] == 'o'||
     line[0] == 'u')
    printf("入力された文字は母音です\n");
  else
    printf("入力された文字は子音です\n");

  return 0;

}
      
